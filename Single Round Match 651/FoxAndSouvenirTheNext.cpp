		for(i = 1;i<=sz;i++)
		{
			int a = value[i-1];
			for(k = 0; k<=(sz+1)/2; k++)
			{
				for(j = sum/2;j>= 0;j--)
				{
					if(j-a >= 0 && dp[i-1][k][j-a] )
					{
						dp[i][k+1][j] = true;
						continue;
					}
						
					if(dp[i-1][k+1][j])
					{
						dp[i][k+1][j] = true;
					}
				}
			}
			
		}
		
