class BinaryDistance {
    public:
    int maxDist(int N, int V)
    {
        int ans = 0;
        int cnt=0;
        long long int x = V;
        while(x <= N){
            x *= 2;
            if(x <=N)
            cnt++;
        }
        ans = max(ans, cnt);
        int a = cnt;
        x = V;
        string p = "";
        cnt = 0;
        while(x != 1){
            if(x&1){
                p = "R"+p;
            }else
            p = "L"+p;
            x = x>>1;
            cnt++;
        }
        ans = max(ans, cnt);
        int b = cnt;
        if(V == 1)
            return ans;
        if(p[0] == 'L'){
            x = 3;
            cnt = N >=3;
            while(2*x <= N){
                cnt++;
                x = x<<1;
            }
            ans = max(ans, cnt+b);
        }
        else{
            cnt = 0;
            x = 1;
            while(2*x <= N){
                cnt++;
                x = x<<1;
            }
            ans = max(ans, cnt+b);
        }

        return ans;
    }
};

